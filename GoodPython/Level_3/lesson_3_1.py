#7. На вход программе подается строка из двух слов, записанных через пробел. Необходимо прочитать эту строку
# и сформировать новую строку, продублировав первое слово дважды, а второе - трижды (все слова в результирующей
# строке должны идти через пробел). Результат выведите на экран.
# Программу следует реализовать без использования F-строк, с применением оператора дублирования строк.
str3, str4 = map(str, input().split())
print((str3+" ")*2, (str4+" ")*3, sep='')


#8. На вход программе подаются два целых числа, записанных через пробел.
# Необходимо прочитать эти целочисленные значения в переменные a и b. Затем, сформировать строку вида (без кавычек):
# "Переменная a = <значение>, переменная b = <значение>" используя оператор конкатенации (соединения) строк. Результат (строку) выведите на экран.
# P. S. F-строки в программе не использовать.
