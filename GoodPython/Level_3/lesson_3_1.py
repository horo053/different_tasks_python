#10. На вход программе подается строка из двух слов, записанных через пробел. Необходимо прочитать эту строку и вычислить следующие булевы значения:
# для оператора in (проверки вхождения первого слова во второе);
# для оператора == (сравнения двух слов);
# для оператора > (сравнение на больше первого слова со вторым);
# для оператора < (сравнение на меньше первого слова со вторым).
# Все булевы значения объединить в одну строку через пробел и вывести на экран в порядке их перечисления в задании
a, b = map(str, input().split())
print(a in b, a == b, a > b, a < b)


#11. На вход программе подаются две буквы, записанные через пробел. Необходимо вывести на экран строку в формате (без кавычек):
# "Коды: <буква1> = <код буквы1>, <буква2> = <код буквы2>"
a, b = map(str, input().split())
print(f"Коды: {a} = {ord(a)}, {b} = {ord(b)}")