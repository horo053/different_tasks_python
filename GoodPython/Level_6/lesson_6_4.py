#4. На вход программе подается строка со словами, разделенные пробелом. Необходимо прочитать эту строку,
# разбить на слова и с помощью множества подсчитать число уникальных слов (без учета регистра).
# Результат (число уникальных слов) вывести на экран.
s_str = set(input().lower().split())
count_s_str = 0
for i in s_str:
    count_s_str += 1
print(count_s_str)


#5. На вход программе подается строка, содержащая латинские символы, пробелы и цифры.
# Необходимо прочитать эту строку и выделить из нее все неповторяющиеся цифры (символы от 0 до 9).
# Выведите на экран все найденные уникальные цифры в одну строчку через пробел в порядке возрастания их значений.
# Если цифры отсутствуют, то вывести строку "НЕТ".
str1 = input()
s_int = set()
