#2. На вход программе подается целое число. Необходимо его прочитать и переменной msg присвоить строку:
# "кратно 3", если введенное число кратно 3;
# "не кратно 3", если введенное число не кратно 3.
# Программу реализовать с использованием тернарного оператора. Значение переменной msg вывести на экран.
int1 = int(input())
msg = "кратно 3" if int1 % 3 == 0 else "не кратно 3"
print(msg)


#3. На вход программе подается слово (строка). Необходимо ее прочитать и переменной msg присвоить строку "палиндром",
# если введенное слово является палиндромом (одинаково читается и вперед и назад), иначе присвоить строку "не палиндром".
# Проверку на палиндром проводить без учета регистра. Программу реализовать с помощью тернарного условного оператора.
# Значение переменной msg отобразить на экране.
str1 = input()
msg1 = "палиндром" if str1.lower() == str1.lower()[::-1] else "не палиндром"
print(msg1)


