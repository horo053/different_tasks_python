#8. Объявите в программе функцию с одним параметром, которая проверяет корректность переданного
# ей email-адреса в виде строки. Полагается, что адрес верен, если он обязательно содержит символы '@' и '.',
# а все остальные символы могут принимать значения: 'a-z', 'A-Z', '0-9' и '_'.
# Если email верен, то функция выводит "ДА", иначе "НЕТ". После объявления функции прочитайте (с помощью функции input)
# строку с email-адресом и вызовите функцию с этим аргументом.
def return_email(email):
    alphabet = "qwertyuiopasdfghjklzxcvbnmQWERTYUIOPASDFGHJKLZXCVBNM0123456789_"
    if "@" in email and "." in email:
        email = email.replace("@", "")
        email = email.replace(".", "")
        for i in email:
            if i in alphabet:
                flag = True
            else:
                flag = False
                break
    else:
        flag = False
    if flag == True: print("ДА")
    else: print("НЕТ")

return_email(input())