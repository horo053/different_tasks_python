#6.Объявите в программе функцию, которая в качестве параметра принимает список (list), находит максимальное,
# минимальное и сумму значений этого списка и выводит результат на экран в виде строки (без кавычек):
# "Min = v_min, max = v_max, sum = v_sum"
# где v_min, v_max, v_sum - вычисленные значения минимального, максимального и суммы значений списка.
# После объявления функции прочитайте (с помощью функции input) список целых чисел,
# записанных в одну строку через пробел, и вызовите функцию с передачей ей этого списка.
def return_min_max_sum(v):
    v_min = min(v)
    v_max = max(v)
    v_sum = sum(v)
    print(F"Min = {v_min}, max = {v_max}, sum = {v_sum}")
return_min_max_sum(list(map(int, input().split())))


