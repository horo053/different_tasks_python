# Дополнительно на вход программе подаются еще пункты меню в виде строк в формате:
# название_1=url_1
# ...
# название_N=url_N
# В программе уже реализовано их считывание и сохранение в списке:
# lst_in = list(map(str.strip, sys.stdin.readlines()))
# Необходимо информацию из списка lst_in преобразовать в словарь того же формата, что и menu,
# и добавить в конец словаря menu, используя оператор распаковки для словарей.
# На результирующий словарь должна вести переменная menu.
# P.S. В программе ничего выводить на экран не нужно! Только сформировать словарь menu.
menu = {'Главная': 'home', 'Архив': 'archive', 'Новости': 'news'}
lst_in = list(map(str.strip, sys.stdin.readlines()))
d = {}
lst_in = [it.split("=") for ind, it in enumerate(lst_in)]
lst_in = [{it[0]: it[1]} for ind, it in enumerate(lst_in)]
for i in lst_in:
    d.update(**i)
menu = {**menu, **d}
print(menu)

