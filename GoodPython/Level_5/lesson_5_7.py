#7. На вход программе подаются вещественные числа, записанные через пробел. Необходимо их прочитать и сохранить в списке.
# Затем, с помощью list comprehension сформировать еще один список, состоящий из элементов исходного списка,
# имеющих четные индексы (то есть, выбрать все элементы с четными индексами).
# Элементы полученного списка вывести в одну строчку через пробел.
float_lst = [float(i) for i in input().split()]
ch_index = [float_lst[i] for i in range(len(float_lst)) if i % 2 == 0]
print(*ch_index)


#8. На вход программе подаются два списка целых чисел одинаковой длины, каждый с новой строки
# Необходимо их прочитать и каждый сохранить в своем отдельном списке. Затем, с помощью list comprehension сформировать
# третий список, состоящий из суммы соответствующих пар чисел введенных списков.
# Элементы полученного списка вывести в одну строчку через пробел.
int_lst_one = [int(i) for i in input().split()]
int_lst_two = [int(i) for i in input().split()]
finish_list = [int_lst_one[i] + int_lst_two[i] for i in range(len(int_lst_one))]
print(*finish_list)


#9. На вход программе подается строка в формате:
# <город 1> <численность населения 1> <город 2> <численность населения 2> ... <город N> <численность населения N>
# Необходимо прочитать эту строку и с помощью list comprehension сформировать список lst, содержащий вложенные списки из пар:
# [[<город 1>, <численность населения 1>], [<город 2>, <численность населения 2>], ...]
# Численность населения - целое число в тыс. человек. Выведите полученный список командой: print(lst)
lst_str = [i for i in input().split()]
finish_list = [[lst_str[i], int(lst_str[i+1])] for i in range(0,len(lst_str),2)]
print(finish_list)