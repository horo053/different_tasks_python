# изложенной в видео-уроке к этой практике. На вход программе поступает таблица целых чисел,
# чтение которой уже реализовано в программе
# Нужно транспонировать список lst_in (строки заменяются на столбцы) и результат сохранить в списке A.
# Отобразите полученный список A с помощью следующей конструкции
# for row in A:
#     print(*row)
s = sys.stdin.readlines()
A = [list(map(int, x.strip().split())) for x in s]
A = [[row[i] for row in A] for i in range(len(A[0]))]
for row in A:
    print(*row)