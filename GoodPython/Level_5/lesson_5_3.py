#8. На вход программе подается натуральное число n. Прочитайте это число и с помощью цикла for определите
# является ли оно простым (то есть, делится нацело только на само себя и на 1).
# Вывести на экран строку "ДА", если n простое и строку "НЕТ" в противном случае.
n2 = int(input())
count = 0
for i in range(1,n2+1):
    if n2 % i == 0: count += 1
if count > 2: print("НЕТ")
else: print("ДА")


#9. На вход программе подается строка с названиями городов, записанных в одну строчку через пробел.
# Необходимо прочитать эту строку и сформировать список из названий городов.
# Переберите полученный список с помощью цикла for и определите, начинается ли название следующего города
# на последнюю букву предыдущего города в списке. Если последними встречаются буквы 'ь', 'ъ', 'ы',
# то берется следующая с конца буква. Вывести на экран "ДА", если последовательность
# удовлетворяет этому правилу и "НЕТ" в противном случае.
city2 = input().split()
for i in range(len(city2)-1):
    if city2[i][-1] == "ь" or city2[i][-1] == "ъ" or city2[i][-1] == "ы":
        if city2[i][-2] == city2[i+1][0].lower():
            res = "ДА"
    elif city2[i][-1] == city2[i+1][0].lower():
        res = "ДА"
    else:
        res = "НЕТ"
        break
print(res)


