# 5. На вход программе подается натуральное число n. Напишите программу, которая вычисляет и выводит рациональное число,
# равное значению выражения: 1/1**2 + 1/2**2 + 1/3**2 + ... + 1/n**2
n = int(input())
sum = 0
for i in range(1, n+1):
    sum += Fraction(1, i**2)
print(sum)


# 6. На вход программе подается натуральное число n. Напишите программу, которая вычисляет и выводит рациональное число,
# равное значению выражения: 1/1! + 1/2! + 1/3! + ... + 1/n!
n = int(input())
sum = 0
for i in range(1, n+1):
    sum += Fraction(1, f(i))
print(sum)
