# 4. Даны две дроби в формате a/b. Напишите программу, которая вычисляет и выводит их сумму, разность, произведение и частное.
m = input()
n = input()
print(f"{m} + {n} = {Fraction(Fraction(m) + Fraction(n))}")
print(f"{m} - {n} = {Fraction(Fraction(m) - Fraction(n))}")
