# 6. Напишите функцию info_kwargs(), которая принимает произвольное количество именованных аргументов и печатает
# именованные аргументы в соответствии с образцом: <имя аргумента>: <значение аргумента>, при этом имена аргументов
# следуют в алфавитном порядке (по возрастанию).
def info_kwargs(**kwargs):
    d = dict(sorted(kwargs.items()))
    for key in d:
        print(f"{key}: {d[key]}")
info_kwargs(first_name='Timur', last_name='Guev', age=28, job='teacher')