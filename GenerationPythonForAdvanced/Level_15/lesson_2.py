# 1. Напишите функцию count_args(), которая принимает произвольное количество аргументов и возвращает количество
# переданных в нее аргументов.
def count_args(*args):
    return len(args)


# 2. Напишите функцию sq_sum(), которая принимает произвольное количество числовых аргументов и возвращает сумму их квадратов.
def sq_sum(*args):
    return sum(i**2 for i in args)


# 3. Напишите функцию mean(), которая принимает произвольное количество аргументов и возвращает среднее арифметическое
# переданных в нее числовых (int или float) аргументов.
def mean(*args):
    l = [i for i in args if type(i) in (int, float)]
    if len(l) > 0 and sum(l) > 0:
        return sum(l) / len(l)
    else:
        return 0.0


# 4. Напишите функцию greet(), которая принимает произвольное количество аргументов строк имен (как минимум одно) и
# возвращает приветствие в соответствии с образцом:Hello, Timur and Roman and Ruslan!
def greet(*args):
    s = f'Hello, {args[0]}'
    for i in range(1, len(args)):
        s += f' and {args[i]}'
    return s + '!'


