# затем k строк — поисковые запросы. Напишите программу, которая выводит все введенные строки, в которых встречаются
# одновременно все поисковые запросы. Примечание. Поиск не должен быть чувствителен к регистру символов.
n = int(input())
lst = [input() for _ in range(n)]
k = int(input())
st = [input() for _ in range(k)]
for i in lst:
    c = 0
    for j in st:
        if j.lower() in i.lower():
            c += 1
    if c >= len(st):
        print(i)


