del lst[lst.index(max(lst))]
print(*lst, sep='\n')


# 4. На вход программе подается натуральное число n, а затем n строк. Напишите программу, которая выводит только
# уникальные строки, в том же порядке, в котором они были введены.
n = int(input())
lst = []
for _ in range(n):
    s = input()
    if s not in lst:
        lst.append(s)
print(*lst, sep="\n")


