# 8. На вход программе подаётся натуральное число n. Напишите программу вычисления знакочередующейся суммы:
# 1 - 2 + 3 - 4 + 5 - 6 + ... + (-1)**(n+1) * n
n = int(input())
c = 0
for i in range(n+1):
    if i % 2 != 0:
        c += i
    else:
        c -= i
print(c)


# 9. На вход программе подается натуральное число n, а затем n различных натуральных чисел последовательности,
# каждое на отдельной строке. Напишите программу, которая выводит наибольшее и второе наибольшее число последовательности.
n = int(input())
a = []
for i in range(n):
    k = int(input())
    a.append(k)
print(max(a))
a.remove(max(a))
