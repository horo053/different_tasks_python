import math
# 1. На вход программе подаются два целых числа a и b (a≤b). Напишите программу, которая подсчитывает количество
# чисел в диапазоне от a до b (включительно), куб которых оканчивается на 4 или 9.
a, b = int(input()), int(input())
c = 0
for i in range(a, b+1):
    if (i**3) % 10 == 4 or (i**3) % 10 == 9:
        c += 1
print(c)


# 2. На вход программе подается натуральное число n, а затем n целых чисел, каждое на отдельной строке.
# Напишите программу, которая подсчитывает сумму введенных чисел (не включая само число n).
n = int(input())
c = 0
for i in range(n):
    i = int(input())
    c += i
print(c)


# 3. На вход программе подается натуральное число n. Напишите программу, которая вычисляет значение выражения:
# (1 + 1/2 + 1/3 + ... + 1/n) - ln(n)
n = int(input())
c = 0
for i in range(1, n+1):
    c = c + (1 / i)
print(c - math.log(n))


# 4. На вход программе подается натуральное число n. Напишите программу, которая подсчитывает сумму тех чисел от 1 до n
# (включительно), квадрат которых оканчивается на 2, 5 или 8.
n = int(input())
